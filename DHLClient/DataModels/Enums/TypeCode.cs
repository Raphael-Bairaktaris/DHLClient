namespace DHLClient
{
    /// <summary>
    /// Provides enumeration over the type code
    /// </summary>
    public enum TypeCode
    {
        /// <summary>
        /// Total tax for the shipment
        /// </summary>
        STTXA = 0,

        /// <summary>
        /// Total discount for the shipment
        /// </summary>
        STDIS = 1,

        /// <summary>
        /// Net shipment / weight charge
        /// </summary>
        SPRQT = 2
    }
}
